
package anisulislam.com.tutorial;


public class Assignment_Demo {
    public static void main(String[] args) {
        int x, y;
        
        x = 3;
        y = 2;
        
        x += y;     // x = x + y = 5 ;
        System.out.println(" X = "+x);
        
        x -= y;     // x = x - y = 3 ;
        System.out.println(" X = "+x);
        
        x *= y;     // x = x * y = 6 ;
        System.out.println(" X = "+x);
        
        x /= y;     // x = x / y = 3 ;
        System.out.println(" X = "+x);
        
        x %= y;     // x = x % y = 1 ;
        System.out.println(" X = "+x);
        
    }
}
